
namespace relational{
    public class RelationalOperations{
        public bool greaterThanOperator(int num1, int num2){
            return num1 > num2;
        }
        public bool lessThanOperator(int num1, int num2){
            return num1 < num2;
        }
        public bool greaterOrEqualToOperator(int num1, int num2){
            return num1 >= num2;
        }
        public bool lessOrEqualToOperator(int num1, int num2){
            return num1 <= num2;
        }
        public bool equalToOperator(int num1, int num2){
            return num1 <= num2;
        }
        public bool notEqualToOperator(int num1, int num2){
            return num1 <= num2;
        }
    }
}